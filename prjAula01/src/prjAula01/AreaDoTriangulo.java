package prjAula01;

public class AreaDoTriangulo {
	
	//declaração do projeto main
	public static void main(string[]args) {
		//declaração de variaveis
		int base,altura;
		doublearea;
		
		//atribuição de valor
		base = 10;
		altura = 6;
		
		//procesamento
		area = (base * altura)/2;
		
		//exibiçao de valores
		System.out.println("10 * 6 =" + area);
		
	}//fim do método main
	

}//fim da classe
